public class minandmaxoftwo {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 40;

        int min = Math.min(num1, num2);
        int max = Math.max(num1, num2);

        System.out.println("Minimum number is: " + min);
        System.out.println("Maximum number is: " + max);
    }
}

